class Calculator:

    def mult(self, val1, val2):
        return val1 * val2

    def add(self, val1, val2):
        return val1 + val2

    def sub(self, val1, val2):
        return val1 - val2

    def divide(self, val1, val2):
        return val1 / val2

    def pow(self, val1, val2):
        return val1 ** val2

    def reminder(self, val1, val2):
        return val1 % val2
